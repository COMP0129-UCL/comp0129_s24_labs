#include <moveit_solution/moveit_solution.h>

///////////////////////////////////////////////////////////////////////////////

SrvClass::SrvClass(ros::NodeHandle& nh)
{
  /* Constructor function, this is run only when an object of the class is first
  created. The aim of this function is to initialise the class */

  nh_ = nh;

  // namespace for our ROS services, they will appear as "/namespace/srv_name"
  std::string service_ns = "/moveit_solution";

  // advertise the services available from this node
  set_arm_srv_ = nh_.advertiseService(service_ns + "/set_arm",
    &SrvClass::setArmCallback, this);
  set_gripper_srv_ = nh_.advertiseService(service_ns + "/set_gripper",
    &SrvClass::setGripperCallback, this);
  add_collision_srv_ = nh_.advertiseService(service_ns + "/add_collision",
    &SrvClass::addCollisionCallback, this);
  remove_collision_srv_ = nh_.advertiseService(service_ns + "/remove_collision",
    &SrvClass::removeCollisionCallback, this);
  pick_srv_ = nh_.advertiseService(service_ns + "/pick",
    &SrvClass::pickCallback, this);

  ROS_INFO("MoveIt! services initialisation finished, namespace: %s", 
    service_ns.c_str());
}

///////////////////////////////////////////////////////////////////////////////

bool
SrvClass::setArmCallback(moveit_solution::set_arm::Request &request,
  moveit_solution::set_arm::Response &response)
{
  /* This service sets the panda arm into a specific pose */

  bool success = moveArm(request.pose);

  response.success = success;

  return success;
}

///////////////////////////////////////////////////////////////////////////////

bool
SrvClass::setGripperCallback(moveit_solution::set_gripper::Request &request,
  moveit_solution::set_gripper::Response &response)
{
  /* This service sets the gripper fingers to a specific width */

  bool success = moveGripper(request.finger_distance);

  response.success = success;

  return success;
}

///////////////////////////////////////////////////////////////////////////////

bool
SrvClass::addCollisionCallback(moveit_solution::add_collision::Request &request,
  moveit_solution::add_collision::Response &response)
{
  /* This service puts a collision object into the planning scene */

  addCollisionObject(request.object_name, request.centre, request.dimensions,
    request.orientation);

  response.success = true;

  return true;
}

///////////////////////////////////////////////////////////////////////////////

bool 
SrvClass::removeCollisionCallback(moveit_solution::remove_collision::Request 
  &request, moveit_solution::remove_collision::Response &response)
{
  /* This service removes a collision object */

  removeCollisionObject(request.object_name);

  response.success = true;

  return true;
}

///////////////////////////////////////////////////////////////////////////////

bool
SrvClass::pickCallback(moveit_solution::pick::Request &request,
  moveit_solution::pick::Response &response)
{
  /* This service picks an object with a given pose */

  bool success = pick(request.grasp_point);

  response.success = success;

  return success;
}

///////////////////////////////////////////////////////////////////////////////

bool
SrvClass::moveArm(geometry_msgs::Pose target_pose)
{
  /* This function moves the move_group to the target position */

  // setup the target pose
  ROS_INFO("Setting pose target");
  arm_group_.setPoseTarget(target_pose);

  // create a movement plan for the arm
  ROS_INFO("Attempting to plan the path");
  moveit::planning_interface::MoveGroupInterface::Plan my_plan;
  bool success = (arm_group_.plan(my_plan) ==
    moveit::planning_interface::MoveItErrorCode::SUCCESS);

  // google 'c++ conditional operator' to understand this line
  ROS_INFO("Visualising plan %s", success ? "" : "FAILED");

  // execute the planned path
  arm_group_.move();

  return success;
}

///////////////////////////////////////////////////////////////////////////////

bool
SrvClass::moveGripper(float width)
{
  /* this function moves the gripper fingers to a new position. Joints are:
      - panda_finger_joint1
      - panda_finger_joint2
  */

  // safety checks
  if (width > gripper_open_) width = gripper_open_;
  if (width < gripper_closed_) width = gripper_closed_;

  // calculate the joint targets as half each of the requested distance
  double eachJoint = width / 2.0;

  // create a vector to hold the joint target for each joint
  std::vector<double> gripperJointTargets(2);
  gripperJointTargets[0] = eachJoint;
  gripperJointTargets[1] = eachJoint;

  // apply the joint target
  hand_group_.setJointValueTarget(gripperJointTargets);

  // move the robot hand
  ROS_INFO("Attempting to plan the path");
  moveit::planning_interface::MoveGroupInterface::Plan my_plan;
  bool success = (hand_group_.plan(my_plan) ==
    moveit::planning_interface::MoveItErrorCode::SUCCESS);

  ROS_INFO("Visualising plan %s", success ? "" : "FAILED");

  hand_group_.move();

  return success;
}

